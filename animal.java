package najave;

public class animal {
    type type;
    hair hair;
    size size;
    country country;
    children children;
    tail tail;
    public animal(type type, hair hair, size size, country country, children children, tail tail)
    {
        this.type = type;
        this.hair = hair;
        this.size = size;
        this.country = country;
        this.children = children;
        this.tail = tail;
    }
}
